        // Animated counter for stats
        function animateValue(id, start, end, duration) {
            const obj = document.getElementById(id);
            let startTimestamp = null;
            const step = (timestamp) => {
                if (!startTimestamp) startTimestamp = timestamp;
                const progress = Math.min((timestamp - startTimestamp) / duration, 1);
                obj.innerHTML = Math.floor(progress * (end - start) + start);
                if (progress < 1) {
                    window.requestAnimationFrame(step);
                }
            };
            window.requestAnimationFrame(step);
        }
        
        // Only animate when stats section is in view
        const statsSection = document.querySelector('section.bg-gradient-to-r');
        
        const observer = new IntersectionObserver((entries) => {
            entries.forEach(entry => {
                if (entry.isIntersecting) {
                    animateValue("stat1", 0, 750, 2000);
                    animateValue("stat2", 0, 500, 2000);
                    animateValue("stat3", 0, 50, 2000);
                    animateValue("stat4", 0, 25, 2000);
                    observer.unobserve(entry.target);
                }
            });
        }, {threshold: 0.5});
        
        if (statsSection) {
            observer.observe(statsSection);
        }
        
